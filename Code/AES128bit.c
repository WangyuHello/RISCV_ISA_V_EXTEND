UInt32 RotWord(UInt32 key)
{
	return ((key << 8) & 0xFFFFFFFF) | (key >> 24);
}
UInt32 SubBytes(UInt32 in)
{
	int out3 = S[(in >> 24) & 0x000000ff];
	int out2 = S[(in >> 16) & 0x000000ff];
	int out1 = S[(in >> 8) & 0x000000ff];
	int out0 = S[in & 0x000000ff];
	return (out3 << 24) | (out2 << 16) | (out1 << 8) | out0;
}

UInt32 InvSubBytes(UInt32 in)
{
	int out3 = InvS[(in >> 24) & 0x000000ff];
	int out2 = InvS[(in >> 16) & 0x000000ff];
	int out1 = InvS[(in >> 8) & 0x000000ff];
	int out0 = InvS[in & 0x000000ff];
	return (out3 << 24) | (out2 << 16) | (out1 << 8) | out0;
}

void KeySpan()
{
	for (int i = 0; i < 4; i++) {
		key[i] = init_key[i];
	}
	for (int i = 4; i < 44; i++)
	{
		if (i % 4 == 0)
		{
			UInt32 tmp0 = RotWord(key[i - 1]);
			UInt32 tmp1 = SubBytes(tmp0);
			key[i] = key[i - 4] ^ (tmp1 ^ RCON[i / 4]);
		}
		else
		{
			key[i] = key[i - 4] ^ key[i - 1];
		}
	}
}

