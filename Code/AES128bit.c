UInt32 GFMul2(UInt32 s) {
	UInt32 result = s << 1;
	UInt32 a7 = result & 0x00000100;

	if (a7 != 0) {
		result = result & 0x000000ff;
		result = result ^ 0x1b;
	}

	return result;
}

UInt32 GFMul3(UInt32 s) {
	return GFMul2(s) ^ s;
}

UInt32 GFMul4(UInt32 s) {
	return GFMul2(GFMul2(s));
}

UInt32 GFMul8(UInt32 s) {
	return GFMul2(GFMul4(s));
}

UInt32 GFMul9(UInt32 s) {
	return GFMul8(s) ^ s;
}

UInt32 GFMul11(UInt32 s) {
	return GFMul8(s) ^ GFMul3(s);
}

UInt32 GFMul13(UInt32 s) {
	return GFMul8(s) ^ GFMul4(s) ^ s;
}

UInt32 GFMul14(UInt32 s) {
	return GFMul8(s) ^ GFMul4(s) ^ GFMul2(s);
}

/**
* GF上的二元运算
*/
UInt32 GFMul(UInt32 n, UInt32 s) {
	UInt32 result;

	if (n == 1)
		result = s;
	else if (n == 2)
		result = GFMul2(s);
	else if (n == 3)
		result = GFMul3(s);
	else if (n == 0x9)
		result = GFMul9(s);
	else if (n == 0xb)//11
		result = GFMul11(s);
	else if (n == 0xd)//13
		result = GFMul13(s);
	else if (n == 0xe)//14
		result = GFMul14(s);

	return result;
}
